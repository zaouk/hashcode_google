package hasha;

public class ProductType {
	Integer id, weight;
	
	public ProductType(Integer id, Integer weight)
	{
		this.id=id;
		this.weight=weight;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getWeight() {
		return weight;
	}

	public void setWeight(Integer weight) {
		this.weight = weight;
	}
	
	

}
